#include <stdio.h>

// This is a program to show use of variables, constants, and data types

#define YEAR 2025

int main() {
    int age = 20;
    float height = 5.7;
    char grade = 'B';

    printf("\nAge: %d", age);
    printf("\nHeight: %f", height);
    printf("\nGrade: %c", grade);
    printf("\nYear: %d", YEAR);

    return 0;
}
